/*
蓝桥杯省赛练习：耐力闯关

问题描述
小蓝看学弟们备战省赛实在太辛苦了，决定在训练间隙组织一场“耐力闯关”小游戏，帮大家换换脑子。

一共有 n 个挑战关卡，第 i 个关卡需要消耗 ai 点体力。
不过，每个关卡都有一定的门槛：只有当你在开始这个关卡之前，已经累计消耗的体力不超过 bi 时，才能顺利完成它。

你可以自由决定挑战关卡的顺序，也可以只选择其中一部分关卡参加，并且每个关卡最多只能挑战一次。

小蓝想知道，在安排最优的情况下，最多能够完成多少个关卡。

输入格式
第一行包含一个整数 n (1 <= n <= 100000)，表示关卡的数量。

接下来 n 行，每行包含两个整数 ai, bi (1 <= ai <= 100000, 1 <= bi <= 1000000000)，
分别表示完成第 i 个关卡需要消耗的体力，以及开始该关卡前允许的最大累计体力消耗。

输出格式
输出一个整数，表示最多能够完成的关卡数量。

样例输入
4
3 3
2 2
1 1
10 1

样例输出
3

样例说明
先完成第 3 个关卡，此时开始前累计消耗为 0，不超过限制 1，完成后累计消耗变为 1。
再完成第 2 个关卡，此时开始前累计消耗为 1，不超过限制 2，完成后累计消耗变为 3。
最后完成第 1 个关卡，此时开始前累计消耗为 3，恰好不超过限制 3，完成后累计消耗变为 6。
因此可以按第 3, 2, 1 个关卡的顺序完成 3 个关卡。
*/

#include <bits/stdc++.h>
using namespace std;

// 题解：排序 + 最大堆反悔贪心
int main() {
	int n;
	cin >> n;

	vector<pair<int, int>> v(n);
	for (int i = 0; i < n; i++) {
		cin >> v[i].first >> v[i].second;
	}

	// 按 (a+b) 从小到大排序
	sort(v.begin(), v.end(), [](const pair<int, int>& x, const pair<int, int>& y) {
		return (long long)x.first + x.second < (long long)y.first + y.second;
	});

	long long sum = 0;
	priority_queue<int> pq;

	for (int i = 0; i < n; i++) {
		int a = v[i].first;
		int b = v[i].second;

		sum += a;
		pq.push(a);

		// 检查当前关卡开始前的累计消耗是否超过 b
		if (sum - a > b) {
			// 反悔：移除已选中体力消耗最大的关卡
			sum -= pq.top();
			pq.pop();
		}
	}

	cout << pq.size() << endl;
	return 0;
}

// 【总结】
// 1) 核心模型：反悔贪心。先把关卡加入方案，若违反约束就踢出“代价最大”的已选关卡。
// 2) 排序关键：按 (ai+bi) 升序处理，使得“更紧的约束”先被满足。
// 3) 堆的作用：在必须删一个时，删除 ai 最大的关卡最划算，可最大化保留数量。
// 4) 复杂度：排序 O(n log n)，每个关卡最多入堆出堆一次，总体 O(n log n)。
// 5) 易错点：sum 需要 long long；比较器里也要做 long long 转换避免溢出。

