/*
P16228 [蓝桥杯 2026 省 A] 读取指令

题目描述：
你正在管理一个老旧的线性机械硬盘，该硬盘被划分为 N 个连续的扇区（编号从 1 到 N）。

由于文件系统的特殊分配机制，第 i 个扇区中存储的数据量为 C x i 字节（其中 C 是系统簇大小的常数）。
例如，当 C = 2 时，第 1 到 4 个扇区的数据量分别为 2, 4, 6, 8 字节。

现在，你需要从这块硬盘中恰好读取 W 字节的数据来进行恢复。为了最小化磁头的寻道损耗，你只能向硬盘发送“读取指令”。
每次指令可以指定一个连续的扇区区间 [l, r]，并读取该区间内的所有数据。

请问，要读取恰好 W 字节的数据，最少需要发送多少次读取指令？
注意：每个扇区最多只能被读取一次。如果无论如何也无法凑出恰好 W 字节，请输出 -1。

输入格式：
第一行包含一个整数 T，表示测试用例的组数。
接下来 T 行，每行包含三个整数 N, C, W，相邻整数之间用空格隔开。

输出格式：
对于每组测试用例，输出一行一个整数，表示最少的读取指令次数。如果无法完成，输出 -1。

输入输出样例 #1
输入 #1
3
4 2 10
4 2 16
4 2 7

输出 #1
1
2
-1

说明/提示
【评测用例规模与约定】
对于 30% 的评测用例，1 <= N <= 1000，T = 2。
对于所有的评测用例，1 <= N <= 1e5，2 <= T <= 10，1 <= C <= 100，0 <= W <= 1e9。
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int N, C;
long long W;


int main() {
	int T;  cin>>T;
	while(T--) {
		cin>>N>>C>>W;
		// 特判0
		if(W == 0) {
			cout<<0<<"\n";
			continue;
		}
		if(W % C != 0) {
			cout<<-1<<"\n";
			continue;
		}
		W = W / C;
		vector<long long> v(N+1);
		vector<long long> preSum(N+1);
		preSum[0] = 0;
		for(int i = 1; i <= N; i++) {
			v[i] = i;
			preSum[i] = preSum[i-1] + v[i];
		}
		
		if(preSum[N] < W) {
			cout<<-1<<"\n";
			continue;
		}
		
		int l = 1, r = 1; // 滑动窗口
		long long sum = v[l];
		bool only_one = false;
		while(l <= r && r <= N) {
			if(sum == W) {
				cout<<1<<"\n";
				only_one = true;
				break;
			} else if(sum < W) {
				r++;
				sum += r;  // 窗口右扩张
			} else {
				sum -= l;
				l++;
				if(l > r) { // 保证 left 不会超过 right
					r = l;
					sum = l;
				}
			}
		}
		if(only_one == false) cout<<2<<"\n";  // 答案不是1肯定是2
	}
	return 0;
}

/*
【总结】
1. 通用模板/万能思路：
   这类“从等差权值序列中选若干不重叠连续段，凑出目标和并最小化段数”的题，可先做可达性剪枝，再判断 1 段是否可行，否则尝试 2 段结论。
2. 模板适用特征：
   单个扇区权值呈等差递增（这里是 1..N 乘 C），连续段和可用双指针/前缀和快速判定，且段数上界较小。
3. 标准框架：
   先把 W 按公因子 C 归一化；若超出总和或不能整除直接 -1；再用滑动窗口判断是否存在一个连续段和为 W；若存在输出 1，否则输出 2。
4. 复杂度结论：
   每组数据窗口判定 O(N)，空间 O(N)（当前写法存了数组与前缀和）。
5. 通用易错点：
   先做 W=0 与 W%C!=0 特判；窗口右扩时注意边界；大整数运算要用 long long。
6. 本题差异化补充：
   原始权值是 C*i，先除以 C 后问题等价为在 1..N 中凑和 W，简化判定。
7. 本题特化处理：
   代码先检查总和 preSum[N] 是否不足，再用双指针找单段；找不到时按题解结论输出 2。
*/
