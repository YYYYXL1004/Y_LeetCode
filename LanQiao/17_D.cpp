/*
P16227 [蓝桥杯 2026 省 A] 切割木材

题目描述：
角落里，一台老旧的自动分装机正发出沉闷的轰鸣声。

小蓝站在流水线旁，准备将一批木材送入分装机。机器的挡板间距 L 是唯一可调参数，任何长度超过 L 的木段都会导致传送带卡死。
显然，挡板间距越小，单位时间内的输送密度就越高。因此，小蓝希望将这个挡板间距 L 设定得尽可能小。

眼前有 N 根原始木材，第 i 根的长度为 A_i。小蓝可以对这些木材进行切割以满足长度要求，但由于锯片磨损，他全程最多只能进行 K 次切割。
具体的切割规则如下：
1. 每次切割可以将一根木材切割为两段。
2. 分割后，新产生的两段木材的长度必须为正整数，且长度之和等于原木材的长度。

现在，请你为小蓝找出这个最小可行的挡板间距 L，使得在总切割次数不超过 K 的前提下，切割后所有木段的最大长度不超过 L。

输入格式：
第一行包含两个整数 N 和 K，分别表示原始木材的数量和最大切割次数。
第二行包含 N 个整数 A_1, A_2, ..., A_N，表示每根原始木材的长度。

输出格式：
输出一个整数，表示最小可行的挡板间距 L。

输入输出样例 #1
输入 #1
1 1
5

输出 #1
3

输入输出样例 #2
输入 #2
2 3
9 6

输出 #2
3

说明/提示
【评测用例规模与约定】
对于 10% 的评测用例，1 <= N <= 100，0 <= K <= 3，1 <= A_i <= 10^3。
对于 50% 的评测用例，1 <= N <= 10^3，0 <= K <= 10^5，1 <= A_i <= 10^5。
对于 100% 的评测用例，1 <= N <= 10^6，0 <= K <= 10^9，1 <= A_i <= 10^9。
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int N,K; 
vector<long long> v;

long long f(long long l) { // 使用长度为l的挡板需要切割几次
	long long sum = 0;
	for(int i = 1; i <= N; i++) {
		long long a = (v[i] + l - 1) / l; // 向上取整
		sum += (a-1); 
	}
	return sum;
}

int main() {
	cin>>N>>K;
	v.resize(N+1);
	long long maxn = 0;
	for(int i = 1; i <= N; i++) {
		cin>>v[i];
		maxn = max(v[i], maxn);
	}
	long long l = 1, r = maxn;
	while(l <= r) {  // 二分
		long long mid = l + (r - l)/2;
		if(f(mid) < K) {
			r = mid - 1;
		} else if(f(mid) > K) {
			l = mid + 1;
		} else {
			r = mid - 1;
		}
	}
	cout<<l;
	return 0;
}

/*
【总结】
1. 通用模板/万能思路：
   对“最小化最大值，且存在可行性判定”的问题，优先想到答案二分；先猜一个上限 L，再判断是否能在限制内实现。
2. 模板适用特征：
   当 L 越大越容易满足条件（单调性）时，可把问题转成“找最小可行 L”的经典二分边界题。
3. 标准框架：
   二分 L；判定函数统计每根木材切成每段长度 <= L 至少需要多少刀，总和 <= K 则可行；根据可行性收缩左右边界。
4. 复杂度结论：
   判定一次 O(N)，二分约 O(log maxA)，总复杂度 O(N log maxA)，空间复杂度 O(N)。
5. 通用易错点：
   切割次数公式是 ceil(A_i/L)-1；边界要防死循环；大数据要用 long long，避免乘加溢出。
6. 本题差异化补充：
   N 可到 1e6，必须线性判定 + 对数次二分；K 很大但仍可直接比较总切割次数。
7. 本题特化处理：
   代码里用 (v[i]+l-1)/l 做向上取整，再减 1 累加所需切割次数，配合二分得到最小可行挡板间距。
*/
