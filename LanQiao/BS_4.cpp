/*
蓝桥杯省赛练习：斐波那契取模的第一个达标日

问题描述
小蓝即将准备第十七届蓝桥杯省赛，于是找来了往届省赛真题琢磨高频考点。经过一番研究后，他发现斐波那契数列也是一个非常常见的考点，于是设计了一道题来考考你。

小蓝定义了一个数列，前两项均为 1，从第三项开始，每一项都等于前两项之和。也就是说，这个数列形如：
1, 1, 2, 3, 5, 8, 13, 21, 34, ...

现在，系统会在第 i 天产生一个数值，恰好等于该数列的第 i 项。

对于每天产生的数值，系统都会对 m 取余，得到当天的结果值。如果某一天得到的结果值恰好为 a，那么这一天就被称为“达标日”。

请你求出，第一个达标日是第几天。

如果这样的日子永远不会出现，请输出 -1。

输入格式
输入一行，包含两个整数 m (2 <= m <= 1000000) 和 a (0 <= a < m)。

输出格式
输出一个整数，表示第一个达标日的天数。
如果这样的日子永远不会出现，输出 -1。

样例输入
4 0

样例输出
6

样例说明
斐波那契数列前几项为：1, 1, 2, 3, 5, 8, ...
将这些数对 4 取余，可以得到：1, 1, 2, 3, 1, 0, ...
可以看到，第 6 天得到的结果值恰好为 0，因此答案为 6。
*/

#include <bits/stdc++.h>
using namespace std;

// 题解：按天模拟 F(i) mod m；利用状态 (1,1) 判断进入新周期
int main() {
	int m, a;
	cin >> m >> a;

	if (a == 1) {
		cout << 1 << endl;
		return 0;
	}

	int f1 = 1, f2 = 1;
	for (int i = 3;; i++) {
		int f3 = (f1 + f2) % m;

		if (f3 == a) {
			cout << i << endl;
			return 0;
		}

		// 出现 (1,1) 说明余数序列开始重复，之后不可能首次出现 a
		if (f2 == 1 && f3 == 1) {
			cout << -1 << endl;
			return 0;
		}

		f1 = f2;
		f2 = f3;
	}

	return 0;
}

// 【总结】
// 1) 核心模型：斐波那契余数序列 + 周期检测（Pisano 周期思想）。
// 2) 正确性关键：状态由相邻两项唯一决定；当再次出现 (1,1) 时，后续序列与开头完全相同。
// 3) 因此若到下一个 (1,1) 前都没遇到 a，就永远不会出现，答案是 -1。
// 4) 复杂度：时间 O(T)，T 为遇到下一次 (1,1) 的步数；空间 O(1)。
// 5) 易错点：a==1 时最早是第 1 天，不是第 2 天；循环检测应放在更新前后一致的位置。

