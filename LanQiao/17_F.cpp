/*
P16229 [蓝桥杯 2026 省 A] 外卖配送

题目描述：
午高峰的外卖站点里，骑手小蓝正紧盯着屏幕上待处理的 N 个订单。这些订单必须严格按照系统列表中的固定顺序依次配送，不可打乱或跳过。
为了顺利完成任务，小蓝计划将这 N 个订单分成若干个批次进行配送。

站点内停放着 M 种不同的交通工具，每种工具都具备两个属性：平均路途耗时 A_i 和箱体拥挤系数 B_i。
对于每一个批次，小蓝可以根据该批次订单的数量，从 M 种工具中独立选择一种最合适的。

假设某一个批次包含 L 个订单，且小蓝选用第 i 种交通工具，则该批次的总耗时由以下三部分组成：
1. 路途耗时：L x A_i 秒。
2. 取餐耗时：由于箱内挤压，整理 L 个订单需处理 L(L-1)/2 对挤压关系，每对耗时 B_i，共计 L(L-1)/2 x B_i 秒。
3. 折返耗时：分批配送存在额外的折返成本。处理第一个批次时，由于小蓝默认已在站点装车完毕可以直接出发，此项耗时为 0 秒；
   但从第二个批次开始，每开启一个新的批次，都必须花费固定的 X 秒用于返回站点装载下一批订单。

现在，请你帮助小蓝规划最优的分批方案，并计算出送完 N 个订单所需的最小总耗时。

输入格式：
一行包含三个整数 N、M 和 X，分别表示订单总数、交通工具的种类数，以及固定耗时。
接下来的 M 行，每行包含两个整数 A_i 和 B_i，依次表示第 i 种交通工具的平均路途耗时与箱体拥挤系数。

输出格式：
输出一个整数，表示完成全部 N 个订单配送任务所需的最小总耗时（单位：秒）。

输入输出样例 #1
输入 #1
5 2 40
10 8
2 20

输出 #1
118

说明/提示
【样例说明】
一种最优的方案是分成 2 批配送：
第 1 批（2 单，选工具 2）：路途 2x2=4；取餐 2x1/2x20=20；共 24 秒。
折返：固定耗时 40 秒。
第 2 批（3 单，选工具 1）：路途 3x10=30；取餐 3x2/2x8=24；共 54 秒。
合计 118 秒。

【评测用例规模与约定】
对于 30% 的评测用例，1 <= N, M <= 100。
对于所有评测用例，1 <= N, M <= 5000，1 <= X, A_i, B_i <= 1e9。
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int N, M;
long long X; 
struct mm{
	long long a, b;
};
vector<mm> v;
vector<long long> memo; // 记录一次送i单，花费最短时间
vector<long long> dp;  // 送i单所需要最短时间，不一定是一次

void sum(long long l) {
	long long ans = 1e18;
	for(int i = 1; i <= M; i++) {
		ans = min(ans, l * v[i].a + (((l*(l-1))/2) * v[i].b));
	}
	memo[l] = ans;
}


int main() {
	cin>>N>>M>>X;
	v.resize(M+1);
	for(int i = 1; i <= M; i++) {
		cin>>v[i].a>>v[i].b;
	}
	memo.resize(N+1);
	dp.resize(N+1);
	for(int i = 1; i <= N; i++) { // 预处理memo[i]
		sum(i);
//		debug(memo[i]);
	}
	dp[1] = memo[1];
	for(int i = 2; i <= N; i++) {
		dp[i] = memo[i]; // 初始设为一次送i个
		for(int j = i - 1; j >= i - j; j--) { // 把i分为，(i-1, 1),(i-2, 2),(i-3, 3)等子状态
			dp[i] = min(dp[i], dp[j] + dp[i - j] + X);
		}
	}
	cout<<dp[N];
	return 0;
}

/*
【总结】
1. 通用模板/万能思路：
   对“顺序任务分批 + 每批代价只与批长有关 + 新批次固定开销”的问题，可用区间型 DP：先算单批代价，再做分割转移。
2. 模板适用特征：
   任务顺序不能打乱，目标是最小总成本；一段长度为 L 的成本可独立计算；多开一段会额外增加固定费用。
3. 标准框架：
   先预处理 cost[L]（这里是 memo[L]，表示一次送 L 单的最优工具耗时），再设 dp[i] 为完成前 i 单最小耗时，
   转移为 dp[i] = min(dp[j] + dp[i-j] + X) 或与单段方案 cost[i] 取最小。
4. 复杂度结论：
   预处理 memo 是 O(NM)；DP 枚举拆分约 O(N^2)；总复杂度 O(NM + N^2)，空间 O(N + M)。
5. 通用易错点：
   注意第一批没有折返开销；大数乘法要用 long long；初始化上界要足够大避免被错误覆盖。
6. 本题差异化补充：
   每个批次还能在 M 种工具中二次优化，因此先对固定 L 取一层 min_i，再进入分批 DP。
7. 本题特化处理：
   代码通过 sum(l) 填 memo[l]，之后 dp 从小到大转移并加上 X，最终输出 dp[N]。
*/
