/*
209. 翻硬币

题目描述
小明正在玩一个"翻硬币"的游戏。

桌上放着排成一排的若干硬币。我们用 * 表示正面，用 o 表示反面（是小写字母，不是零）。

比如，可能情形是：**oo***oooo。
如果同时翻转左边的两个硬币，则变为：oooo***oooo。

现在小明的问题是：如果已知了初始状态和要达到的目标状态，每次只能同时翻转相邻的两个硬币，
那么对特定的局面，最少要翻动多少次呢？

我们约定：把翻动相邻的两个硬币叫做一步操作。

输入描述
两行等长的字符串，分别表示初始状态和要达到的目标状态。
每行的长度 < 1000。

输出描述
一个整数，表示最小操作步数。

输入输出样例
输入：
**********
o****o****

输出：
5
*/

#include <bits/stdc++.h>
using namespace std;

int main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);

	string s1, s2;
	cin >> s1 >> s2;

	int cnt = 0;
	for (int i = 0; i < (int)s1.size() - 1; i++) {
		if (s1[i] != s2[i]) {
			s1[i] = (s1[i] == '*') ? 'o' : '*';
			s1[i + 1] = (s1[i + 1] == '*') ? 'o' : '*';
			cnt++;
		}
	}

	cout << cnt;
	return 0;
}

/*
【总结】
1. 通用模板（相邻翻转/前缀纠错贪心）
当操作只会影响当前位置 i 和下一个位置 i+1，且目标是最少操作次数时，
可以从左到右扫描：一旦第 i 位与目标不同，就必须在 i 位置执行一次操作来修正它。

2. 标准框架
从 i=0 扫到 n-2：
- 若当前位已匹配，跳过；
- 若不匹配，翻转 i 和 i+1，答案加一。
最后第 n-1 位会被前一步唯一决定，不需要回头。

3. 正确性直觉
第 i 位只能被操作 i-1 或 i 影响。
当扫描到 i 时，i-1 已经固定，若第 i 位不匹配，唯一可行选择就是立刻执行操作 i。
因此该贪心是唯一且最优的。

4. 复杂度
时间复杂度 O(n)，空间复杂度 O(1)（若原地修改字符串）。

5. 本题差异与易错点
- 循环上界是 n-2（代码写作 i < n-1），因为每次会访问 i+1。
- 字符是小写字母 o，不是数字 0。
- 题目保证可达时直接输出 cnt；若遇到扩展题未保证可达，可在末位再做一次一致性校验。
*/
