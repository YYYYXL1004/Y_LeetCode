/*
蓝桥杯省赛练习：删除 k 位得到最大数（子序列）

问题描述
小蓝即将准备第十七届蓝桥杯省赛，于是找来了往届省赛真题琢磨高频考点。经过一番整理后，他发现“子序列”是一个非常常见的考点，于是设计了一道相关的题目来考考你。

给定一个由 N 位数字组成的正整数，小蓝希望从中删除恰好 k 位数字，剩余数字的相对顺序不能改变。删除完成后，剩下的数字按原顺序拼接成一个新的数。

请你帮助小蓝求出：在恰好删除 k 位数字之后，能够得到的最大的数是多少。

输入格式
第一行包含两个整数 N, k (0 <= k < N <= 300000)，分别表示原数的位数以及需要删除的位数。

第二行包含一个由 N 位数字组成的正整数。

输出格式
输出一个整数，表示删除恰好 k 位数字后能够得到的最大数。

样例输入
6 2
321456

样例输出
3456

样例说明
原数为 321456，需要删除 2 位数字。
删去 2 和 1 后，剩余数字按原顺序组成的数为 3456。
这是所有合法方案中能够得到的最大值。
*/

#include <bits/stdc++.h>
using namespace std;

// 题解：单调栈（维护当前答案尽量字典序大）
int main() {
	int n, k;
	cin >> n >> k;
	string s;
	cin >> s;

	string ans;
	ans.reserve(n);

	for (char c : s) {
		while (!ans.empty() && k > 0 && ans.back() < c) {
			ans.pop_back();
			k--;
		}
		ans.push_back(c);
	}

	// 若遍历结束仍有可删除次数，说明后缀是非增的，直接从末尾删除
	while (k > 0) {
		ans.pop_back();
		k--;
	}

	cout << ans;
	return 0;
}

// 【总结】
// 1) 这是典型“固定删除次数 + 保序 + 取最大/最小”的单调栈模型。
// 2) 为了得到最大数：遇到更大的新数字时，尽可能弹出栈顶较小数字（前提 k > 0）。
// 3) 每个字符最多进栈出栈一次，时间复杂度 O(N)，空间复杂度 O(N)。
// 4) 易错点：遍历完后若 k 还没用完，必须继续从末尾删掉 k 位。

