/*
P1803. 线段覆盖

题目描述
现在各大 oj 上有 n 个比赛，每个比赛的开始、结束的时间点是知道的。

yyy 认为，参加越多的比赛，noip 就能考的越好（假的）。

所以，他想知道他最多能参加几个比赛。

由于 yyy 是蒟蒻，如果要参加一个比赛必须善始善终，而且不能同时参加 2 个及以上的比赛。

输入格式
第一行是一个整数 n，接下来 n 行每行是 2 个整数 ai,bi (ai<bi)，表示比赛开始、结束的时间。

输出格式
一个整数最多参加的比赛数目。

输入输出样例
输入：
3
0 2
2 4
1 3

输出：
2

说明/提示
对于 20% 的数据，n<=10；
对于 50% 的数据，n<=10^3；
对于 70% 的数据，n<=10^5；
对于 100% 的数据，1<=n<=10^6，0<=ai<bi<=10^6。
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int main() {
	int cnt = 1; 
	int n;  cin>>n;
	vector<vector<int>> v(n, vector<int>(2));
	for(int i = 0; i < n; i++) {
		cin>>v[i][0]>>v[i][1];
	}
	auto cmp = [](const vector<int>& v1, const vector<int>& v2) {
		if(v1[1] == v2[1]) return v1[0] < v2[0];
		return v1[1] < v2[1];   // 区间调度，右端点排序，区间合并，左端点合并	
	};
	sort(v.begin(), v.end(), cmp);
	int ed = v[0][1];
	for(int i = 1; i < n; i++) {
		if(v[i][0] < ed) continue;
		else {
			cnt++;
			ed = v[i][1];
		}
	}
	cout<<cnt;
	return 0;
}

/*
【总结】
1. 通用模板（区间调度最大可选数）
目标是“选最多互不重叠区间”时，经典做法是按右端点从小到大排序，再贪心选择。

2. 标准框架
- 先按结束时间升序排序（结束早的优先）；
- 维护当前已选最后一个区间的结束点 ed；
- 依次扫描区间，若新区间起点 >= ed 就选入并更新 ed。

3. 正确性直觉
每次优先选结束最早的区间，会给后续保留最大可用时间窗口，
因此不会比任何其他选择差，这是区间调度的经典最优贪心策略。

4. 复杂度
排序 O(nlogn)，线性扫描 O(n)，总复杂度 O(nlogn)。

5. 本题差异与易错点
- 本题允许端点相接（如 [0,2] 和 [2,4] 可同时参加），判断条件应是 start >= ed。
- n 很大时要用 fast io。
- 若直接从第一个区间开始计数，注意 n=0 的边界；本解法用 cnt=0 统一处理更稳。
*/
