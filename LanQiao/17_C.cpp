/*
P16226 [蓝桥杯 2026 省 A] 拦截程序

题目描述：
联邦安全局截获了一起正在进行的黑客攻击：一个携带核心机密的数据包，正沿着一条线性的光纤隧道被秘密传输。

这条光纤隧道的总长度为 L，可将其视为一条从坐标 0（左端）到坐标 L（右端）的线段。
情报显示，该数据包在光纤内的移动速度为恒定值 V，且已经传输了整整 T 秒，即目前在光纤内推进了 V x T 的距离。

黑客的入侵源头尚不明确，因此数据包当前的位置存在两种可能：
1. 情况 A（左端入侵）：数据包位于坐标 P_A = V x T 处。
2. 情况 B（右端入侵）：数据包位于坐标 P_B = L - V x T 处。

你需要在光纤上选择一个整数坐标 P（0 <= P <= L）部署拦截程序。

为了使拦截方案尽可能稳妥，你需要衡量每一个坐标 P 的偏差：
即 P 到 P_A 的距离与 P 到 P_B 的距离中的较大者。

现在，请寻找一个最优的整数坐标 P，使得这个偏差值最小，并输出该最小值。

输入格式：
第一行包含一个整数 C，表示测试用例的数量。
接下来的 C 行，每行包含三个整数 L, V, T，分别表示光纤隧道的总长度、数据包的移动速度，以及已经传输的时间。

输出格式：
对于每个测试用例，输出一行一个整数，表示最小偏差值。

输入输出样例 #1
输入 #1
3
100 2 10
51 5 2
200 10 10

输出 #1
30
16
0

说明/提示
【样例说明】
对于第一组数据 L = 100, V = 2, T = 10：数据包推进了 20。它可能在情况 A（坐标 20）处，也可能在情况 B（坐标 80）处。
选择整数坐标 P = 50，无论在哪边，距离都是 30。

对于第二组数据 L = 51, V = 5, T = 2：数据包推进了 10。它可能在情况 A（坐标 10）处，也可能在情况 B（坐标 41）处。
最佳的整数坐标可以选择 P = 25：到 A 的距离是 15，到 B 的距离是 16，较大值为 16。
或者选择 P = 26：到 A 的距离是 16，到 B 的距离是 15，较大值同样是 16。
因此最小的最坏情况距离为 16。

对于第三组数据 L = 200, V = 10, T = 10：数据包推进了 100。无论从哪边开始，它现在都刚好在中心坐标 100 处。
直接将程序部署在 P = 100 处，最大距离为 0。

【评测用例规模与约定】
对于 30% 的评测用例，1 <= C <= 100，1 <= L, V, T <= 1000。
对于所有评测用例，1 <= C <= 1e5，1 <= L, V, T <= 1e12，且保证 V x T <= L。
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int main() {
	int C;  cin>>C;
	while(C--) {
		long long L, V, T; 
		cin>>L>>V>>T;
		long long pa = V * T, pb = L - pa;
		if(pa > pb) swap(pa, pb);
		long long cha = pb - pa;
		if(cha&1) cout<<cha/2 + 1<<"\n";  // 奇数，+1
		else cout<<cha/2<<"\n";
	}
	return 0;
}

/*
【总结】
1. 通用模板/万能思路：
   对“在线段上选一个点，最小化到两个候选位置的最坏距离”这类问题，核心是做 1D minimax，最优点在两端点中点附近。
2. 模板适用特征：
   目标函数是 max(|P-a|, |P-b|)，且 P 受整数约束；通常先排序 a <= b，再只看区间 [a,b] 的中间。
3. 标准框架：
   设 d = b-a，实数最优值为 d/2；若 P 必须为整数，则最优值为 ceil(d/2)。
4. 复杂度结论：
   每组数据只做常数次运算，时间复杂度 O(1)，总复杂度 O(C)，空间复杂度 O(1)。
5. 通用易错点：
   注意先保证 a <= b；奇偶处理要用上取整；数据范围大时用 long long 防止溢出。
6. 本题差异化补充：
   两个候选点由 pa = V*T 与 pb = L - V*T 给出，直接代入模板，答案就是 ceil(|pb-pa|/2)。
7. 本题特化处理：
   代码中先算 cha = pb-pa，再按奇偶输出：奇数输出 cha/2+1，偶数输出 cha/2。
*/
