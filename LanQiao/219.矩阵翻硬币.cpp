/*
219. 矩阵翻硬币

题目描述
小明先把硬币摆成了一个 n 行 m 列的矩阵。

随后，小明对每一个硬币分别进行一次 Q 操作。

对第 x 行第 y 列的硬币进行 Q 操作的定义：将所有第 i*x 行，第 j*y 列的硬币进行翻转。
其中 i 和 j 为任意使操作可行的正整数，行号和列号都是从 1 开始。

当小明对所有硬币都进行了一次 Q 操作后，他发现了一个奇迹：所有硬币均为正面朝上。

小明想知道最开始有多少枚硬币是反面朝上的。

输入描述
输入数据包含一行，两个正整数 n, m，含义见题目描述。
其中 n、m <= 10^1000。

输出描述
输出一个正整数，表示最开始有多少枚硬币是反面朝上的。
*/

#include <iostream>
#include <cmath>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    long long n, m;
    // 考场上如果遇到这种题，直接读 long long 拿部分分
    if (cin >> n >> m) {
        // 注意：计算大整数的平方根，一定要用 sqrtl (针对 long double)，
        // 用普通的 sqrt 可能会在接近 10^18 时产生精度误差导致向下取整错误。
        long long root_n = sqrtl(n);
        long long root_m = sqrtl(m);
        
        long long ans = root_n * root_m;
        cout << ans << "\n";
    }
    
    return 0;
}

/*
【总结】
1. 通用模板（翻转奇偶性）
这类题先固定一个位置，统计它被翻转的次数 t，只看 t 的奇偶性。
若最终状态已知，则可反推初始状态：初始是否反面由 t 的奇偶决定。

2. 本题核心结论
位置 (r,c) 被翻次数 = d(r) * d(c)（d(x) 为约数个数）。
d(x) 为奇数当且仅当 x 是完全平方数。
所以初始为反面的格子数 = floor(sqrt(n)) * floor(sqrt(m))。

3. 复杂度
你的这版代码是 O(1)，直接开方计算。

4. 当前版本定位
本实现是考场 long long 版本（部分分思路），适用于 n,m 在 64 位范围内。
题目原始上界到 10^1000，若要满分需要大整数开方实现。

5. 易错点
- sqrtl 要配合 long double 使用，减少精度误差风险。
- 即便用 sqrtl，极限整数附近仍建议做一次乘法校正（本版未加，走的是考场快写路线）。
*/
