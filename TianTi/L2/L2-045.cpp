/*
L2-045 堆宝塔
分数 25
作者 陈越
单位 浙江大学

堆宝塔游戏是让小朋友根据抓到的彩虹圈的直径大小，按照从大到小的顺序堆起宝塔。
但彩虹圈不一定是按照直径的大小顺序抓到的。聪明宝宝采取的策略如下：

首先准备两根柱子，一根 A 柱串宝塔，一根 B 柱用于临时叠放。
把第 1 块彩虹圈作为第 1 座宝塔的基座，在 A 柱放好。
将抓到的下一块彩虹圈 C 跟当前 A 柱宝塔最上面的彩虹圈比一下，如果比最上面的小，就直接放上去；
否则把 C 跟 B 柱最上面的彩虹圈比一下：
如果 B 柱是空的、或者 C 大，就在 B 柱上放好；
否则把 A 柱上串好的宝塔取下来作为一件成品；
然后把 B 柱上所有比 C 大的彩虹圈逐一取下放到 A 柱上，最后把 C 也放到 A 柱上。

重复此步骤，直到所有的彩虹圈都被抓完。最后 A 柱上剩下的宝塔作为一件成品，
B 柱上剩下的彩虹圈被逐一取下，堆成另一座宝塔。
问：宝宝一共堆出了几个宝塔？最高的宝塔有多少层？

输入格式：
输入第一行给出一个正整数 N（<=10^3），为彩虹圈的个数。
第二行按照宝宝抓取的顺序给出 N 个不超过 100 的正整数，对应每个彩虹圈的直径。

输出格式：
在一行中输出宝宝堆出的宝塔个数，和最高的宝塔的层数。
数字间以 1 个空格分隔，行首尾不得有多余空格。

输入样例：
11
10 8 9 5 12 11 4 3 1 9 15

输出样例：
4 5

样例解释：
宝宝堆成的宝塔顺次为：
10、8、5
12、11、4、3、1
9
15、9
*/

#include<bits/stdc++.h>
using namespace std;

int main() {
	int N;
	cin >> N;

	int cnt = 0;
	int maxn = 0;

	vector<int> A;
	stack<int> B;

	while(N--) {
		int x;
		cin >> x;

		if(A.empty() || A.back() > x) {
			A.push_back(x);
		} else if(B.empty() || x > B.top()) {
			B.push(x);
		} else {
			cnt++;
			maxn = max(maxn, (int)A.size());
			A.clear();

			// 把 B 上所有比 x 大的圈转移到 A，保持 A 仍为严格递减。
			while(!B.empty() && B.top() > x) {
				A.push_back(B.top());
				B.pop();
			}
			A.push_back(x);
		}
	}

	if(!A.empty()) {
		cnt++;
		maxn = max(maxn, (int)A.size());
	}
	if(!B.empty()) {
		cnt++;
		maxn = max(maxn, (int)B.size());
	}

	cout << cnt << " " << maxn;
	return 0;
}

// 【总结】
// 题型通用模板：
// 1. 适用特征：按输入顺序在线处理，主结构不满足条件时需要“暂存+回填”重建新结构。
// 2. 标准框架：主栈/主序列维护目标性质，辅栈暂存冲突元素，触发条件时结算并回搬。
// 3. 正确性抓手：维护不变量（主结构始终严格递减），每个元素只在有限次入/出结构中被处理。
// 4. 复杂度：每个元素最多进出 A、B 各一次，整体 O(N)，空间 O(N)。
// 5. 通用易错点：回搬时必须搬“元素本身”而不是当前输入值；结尾需处理剩余结构的结算。
//
// 本题差异化补充：
// 1. 结算时机是遇到 x 使得 x >= A.top 且 x <= B.top（B 非空），此时当前 A 必须先成品化。
// 2. 回搬规则是只搬 B 中严格大于 x 的圈，再把 x 压入 A，才能继续维持 A 的严格递减。
// 3. 最终答案由三段贡献：过程中的 A 成品数 + 末尾 A（若非空）+ 末尾 B（若非空）。
