/*
L2-033 简单计算器
分数 25
作者 陈越
单位 浙江大学

本题要求你为初学数据结构的小伙伴设计一款简单的利用堆栈执行的计算器。
计算器由两个堆栈组成：一个堆栈 S1 存放数字，另一个堆栈 S2 存放运算符。

每次按下等号键，执行以下操作：
1) 从 S1 中弹出两个数字，顺序为 n1 和 n2；
2) 从 S2 中弹出一个运算符 op；
3) 执行计算 n2 op n1；
4) 将结果压回 S1。

直到两个堆栈都为空时，计算结束，最后结果显示在屏幕上。

输入格式：
输入首先在第一行给出正整数 N（1 < N <= 10^3），为 S1 中数字的个数。
第二行给出 N 个绝对值不超过 100 的整数；
第三行给出 N-1 个运算符（仅考虑 +、-、*、/）。

输出格式：
将输入数字和运算符按给定顺序分别压入 S1 和 S2 后，按规则执行计算并输出最终结果。
所有计算只取整数部分。题目保证中间与最终结果绝对值不超过 10^9。

若执行除法时分母为 0，则输出：ERROR: X/0（X 为当时分子），并立即结束程序。

输入样例1：
5
40 5 8 3 2
/ * - +

输出样例1：
2

输入样例2：
5
2 5 8 4 4
* / - +

输出样例2：
ERROR: 5/0
*/

#include<bits/stdc++.h>
using namespace std;

int main() {
	int N;
	cin >> N;

	stack<int> s1;
	stack<char> s2;

	for(int i = 0; i < N; i++) {
		int x;
		cin >> x;
		s1.push(x);
	}

	for(int i = 0; i < N - 1; i++) {
		char x;
		cin >> x;
		s2.push(x);
	}

	bool divZero = false;

	while(!s2.empty()) {
		int n1, n2, n3;
		n1 = s1.top();
		s1.pop();
		n2 = s1.top();
		s1.pop();

		char op;
		op = s2.top();
		s2.pop();

		if(op == '/') {
			if(n1 == 0) {
				cout << "ERROR: " << n2 << "/0";
				divZero = true;
				break;
			} else {
				n3 = n2 / n1;
				s1.push(n3);
			}
		} else if(op == '*') {
			n3 = n2 * n1;
			s1.push(n3);
		} else if(op == '+') {
			n3 = n2 + n1;
			s1.push(n3);
		} else if(op == '-') {
			n3 = n2 - n1;
			s1.push(n3);
		}
	}

	if(!divZero) cout << s1.top();
	return 0;
}

// 【总结】
// 一、题型通用模板（双栈按后进先出规则模拟）
// 1) 适用特征：题目已明确给出“数字栈 + 运算符栈”的执行过程，需要严格还原弹栈顺序。
// 2) 标准流程：
//    - 先按输入顺序分别压入两个栈；
//    - 循环直到运算符栈空：弹 n1、n2 和 op，计算 n2 op n1；
//    - 把结果压回数字栈，最终栈顶即答案。
// 3) 复杂度：每个数字/运算符只进出栈一次，时间 O(N)，空间 O(N)。
// 4) 通用易错点：
//    - 运算顺序写反成 n1 op n2；
//    - 忘记处理中途非法操作导致继续计算。

// 二、本题差异化补充
// 1) 本题要求整除结果的整数部分，C++ int 除法天然满足该要求（向 0 截断）。
// 2) 除零时需输出固定格式 ERROR: X/0 并立刻结束，不再输出最终结果。
// 3) 输入给的是“压栈顺序”，因此最终计算次序是逆序触发，正好由 stack 自动体现。
