/*
L2-037 包装机
分数 25
作者 陈越
单位 浙江大学

一种自动包装机的结构如下：机器中有 N 条轨道，轨道上放有物品；轨道下方有一个筐。
按下某条轨道 i（1<=i<=N）的按钮时，会把该轨道尽头的一件物品推入筐中；
按下 0 号按钮时，机械手会把筐顶部一件物品取出并放到流水线上。

特殊规则：
1) 筐容量有限。若筐已满且又按下某条轨道按钮，系统会先强制执行一次 0 号键
   （先从筐顶取一件到流水线），再把该轨道物品推入筐中；
2) 若轨道已空，按该轨道按钮无事发生；
3) 若筐为空，按 0 号按钮无事发生。

现给定一系列按钮操作，要求输出流水线上物品的顺序。

输入格式：
第一行给出 3 个正整数 N（<=100）、M（<=1000）和 Smax（<=100），
分别表示轨道条数、每条轨道初始物品数量、筐最大容量。
随后 N 行，每行给出 M 个英文大写字母，表示每条轨道从前到后的初始物品。
最后一行给出按钮序列，以 -1 结束（-1 不处理）。

输出格式：
在一行中按顺序输出流水线上的物品，不含空格。

输入样例：
3 4 4
GPLT
PATA
OMSA
3 2 3 0 1 2 0 2 2 0 -1

输出样例：
MATA
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int main() {
	int N, M, S;
    cin>>N>>M>>S;
    vector<queue<char>> v(N+1);
    string ans = "";
    for(int i = 1; i <= N; i++) {
        for(int j = 1; j <= M; j++) {
            char ch;  cin>>ch;
            v[i].push(ch);
        }
    }
    
    int x; char ch;
    stack<char> s;
    while(cin>>x) {
        if(x == -1) break;
        if(x == 0 && !s.empty()) {
            ans += s.top();
            s.pop();
        } else if(!v[x].empty()) {
            ch = v[x].front();
            v[x].pop();
            if(s.size() == S) {
                ans += s.top();
                s.pop();
            }
            s.push(ch);
        }
    }
    cout<<ans;
	return 0;
}

// 【总结】
// 一、题型通用模板（生产线/缓存区模拟）
// 1) 适用特征：存在“输入容器 -> 中间缓存 -> 输出序列”的逐步按指令仿真过程。
// 2) 常规模板：
//    - 用队列表示每条轨道（先进先出取尽头物品）；
//    - 用栈表示筐（后进先出取顶部物品）；
//    - 逐条读取操作并严格按题目规则更新状态；
//    - 每次真正取到流水线的物品立刻追加到答案串。
// 3) 复杂度：每件物品最多进出一次，整体 O(N*M + Q)，额外空间 O(N*M + Smax)。
// 4) 通用易错点：
//    - 忽略“无事发生”分支（空轨道/空筐）；
//    - 缓冲区满时处理顺序写错（应先出再入）。

// 二、本题差异化补充
// 1) 本题的“强制 0 号键”是隐式动作：当筐满且按轨道键时，必须先把筐顶输出。
// 2) 操作序列以 -1 结束，-1 仅作终止标记，不参与处理。
// 3) 题目保证至少会输出一件物品，但实现上无需依赖该条件，按规则模拟即可。
