/*
T739294 2026 L2-1 游戏

你有四个正整数 n, a, b, c，并准备用它们玩一个简单的小游戏。

在一轮游戏操作中，你可以选择将 n 减去 a，或是将 n 减去 b。游戏将会进行多轮操作，直到当 n <= c 时游戏结束。

你想知道游戏结束时有多少种不同的游戏操作序列。两种游戏操作序列不同，当且仅当游戏操作轮数不同，或是某一轮游戏操作中，一种操作序列选择将 n 减去 a，而另一种操作序列选择将 n 减去 b。如果 a = b，也认为将 n 减去 a 与将 n 减去 b 是不同的操作。

由于答案可能很大，你只需要求出答案对 10^9 + 7 取模的结果。

输入格式：
一行四个整数 n, a, b, c（1 <= a, b, c <= n <= 2e5）。

输出格式：
输出一行一个整数表示答案。

输入样例：
1 1 1 1
114 51 4 1
114514 191 9 810

输出样例：
1
176
384178446
*/

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

const int MOD = 1e9 + 7;

int main() {
    int n, a, b, c;
    cin >> n >> a >> b >> c;

    // dp[i]：从数值 i 开始，首次使数值 <= c 的操作序列个数；i<=c 时视为已结束，序列数为 1
    vector<int> dp(n + 1, 0);
    for (int i = 0; i <= min(n, c); ++i) {
        dp[i] = 1;
    }
    for (int i = c + 1; i <= n; ++i) {
        // max(0, idx) 处理减到负数的情况，等价于“一步结束”贡献 1
        int ways_a = dp[max(0, i - a)];
        int ways_b = dp[max(0, i - b)];
        dp[i] = (ways_a + ways_b) % MOD;
    }

    cout << dp[n] << "\n";
    return 0;
}

/*
总结：
本题考察线性 DP，计数“首次到达终止条件”的操作序列方案数。

1. 题目分析：
   - 从 n 出发，每步可减 a 或减 b，直到当前值 <= c 结束
   - 不同序列 = 轮数不同或某一轮选择不同（减 a 与减 b 视为不同操作）
   - 求方案数对 10^9+7 取模

2. 解题思路：
   - 定义 F(v) = 从数值 v 开始，第一次使数值 <= c 时的不同操作序列个数
   - v > c 时：当前步可减 a 或减 b；若一步后 <= c 则贡献 1，否则贡献 F(下一状态)
   - 递推：F(v) = (v-a<=c ? 1 : F(v-a)) + (v-b<=c ? 1 : F(v-b))，模 MOD

3. 边界与答案：
   - 统一边界：dp[0..min(n,c)] = 1（已到 <= c 视为 1 种）；再从 c+1 递推到 n
   - n <= c 时无需特判，dp[n] 已在边界中为 1

4. 实现技巧：
   - 用 dp[max(0, i-a)]、dp[max(0, i-b)] 统一处理“一步减到非正”的情况（等价贡献 1，因 dp[0]=1）
   - vector 按需开 n+1，避免固定 MAXN；取模用 (ways_a + ways_b) % MOD 即可，和不超过 2MOD

5. 时间复杂度：O(n)
*/
