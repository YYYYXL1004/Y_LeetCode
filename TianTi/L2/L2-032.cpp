/*
L2-032 彩虹瓶
分数 25
作者 陈越
单位 浙江大学

彩虹瓶的制作过程（并不）是这样的：先把一大批空瓶铺放在装填场地上，
然后按照一定的顺序将每种颜色的小球均匀撒到这批瓶子里。

假设彩虹瓶里要按顺序装 N 种颜色的小球（编号为 1 到 N）。现在工厂里有每种颜色
的小球各一箱，工人需要一箱一箱地将小球从工厂里搬到装填场地。
如果搬来的这箱小球正好是可以装填的颜色，就直接拆箱装填；
如果不是，就把箱子先码放在一个临时货架上，码放的方法就是一箱一箱堆上去。

当一种颜色装填完以后，先看看货架顶端的一箱是不是下一个要装填的颜色，
如果是就取下来装填，否则去工厂里再搬一箱过来。

若货架顶不是需要颜色且工厂后续也无法在不破坏货架顺序的情况下继续，任务失败。
另外货架容量有限，若堆积超过容量，也会失败。

输入格式：
输入首先在第一行给出 3 个正整数，分别是彩虹瓶的颜色数量 N（1 < N <= 10^3）、
临时货架的容量 M（< N）、以及需要判断的发货顺序数量 K。

随后 K 行，每行给出 N 个数字，是 1 到 N 的一个排列，对应工厂的发货顺序。

输出格式：
对每个发货顺序，如果工人可以顺利完工，输出 YES；否则输出 NO。

输入样例：
7 5 3
7 6 1 3 2 5 4
3 1 5 4 2 6 7
7 6 5 4 3 2 1

输出样例：
YES
NO
NO
*/

#include<bits/stdc++.h>
using namespace std;

int main() {
	int N, M, K;
	cin >> N >> M >> K;

	while(K--) {
		stack<int> s;
		int need = 1;
		bool possible = true;

		for(int i = 0; i < N; i++) {
			int x;
			cin >> x;

			if(!possible) continue;  // 如果已经失败，后续只读入不处理

			if(x == need) {
				need++;

				while(!s.empty() && s.top() == need) {
					s.pop();
					need++;
				}
			} else {
				s.push(x);
				if((int)s.size() > M) {
					possible = false;
				}
			}
		}

		if(possible && need == N + 1) cout << "YES" << "\n";
		else cout << "NO" << "\n";
	}

	return 0;
}

// 【总结】
// 一、题型通用模板（受限栈模拟可行性判断）
// 1) 适用特征：目标输出是固定顺序，输入顺序可借助“后进先出”缓冲区调整。
// 2) 标准框架：
//    - need 表示当前需要输出的编号；
//    - 依次读取输入 x：若 x == need 则直接输出，并持续弹栈匹配；
//    - 否则 x 入栈；
//    - 任一步触发约束违例（如栈容量超限）则判失败；
//    - 最后 need 是否走到 N + 1 判定是否完全完成。
// 3) 复杂度：每个元素最多入栈、出栈各一次，时间 O(N)，空间 O(M)。
// 4) 通用易错点：
//    - 忘记在“直接输出后”循环弹栈，会漏掉连续可匹配元素；
//    - 失败后没把该组剩余输入读完，导致后续测试用例读入错位。

// 二、本题差异化补充
// 1) 本题把缓冲区具体化为“货架”，容量上限是 M，因此入栈后要立即检查 size > M。
// 2) 颜色目标顺序固定为 1..N，可直接用 need 从 1 递增模拟。
// 3) 每组数据是一个 1..N 的排列，不需要去重判重，专注栈流程与容量约束即可。
