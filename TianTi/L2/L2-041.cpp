/*
L2-041 插松枝
分数 25
作者 陈越
单位 浙江大学

人造松枝加工场的工人需要将各种尺寸的塑料松针插到松枝干上，做成大大小小的松枝。

工作流程如下：
1) 每人手边有一只小盒子，初始为空；
2) 推送器会按顺序推出松针片；
3) 做一根松枝时，优先尝试从小盒子顶取；若盒子空则从推送器取；
4) 往同一根松枝继续插时，后插入的松针片不能比前一步更大（即保持不增）；
5) 若盒子顶不满足，则尝试从推送器取；若推送器取到仍不满足，则压入盒子。

结束当前松枝并开始下一根的条件：
（1）盒子已满，且推送器当前松针仍不满足要求；此时把当前松枝放入成品篮，
    并将该不满足的松针压回推送器（即不消耗）。
（2）盒子顶不满足，且推送器已空；结束当前松枝。
（3）当前松枝已插满 K 片；结束当前松枝。

输入格式：
第一行给出 3 个正整数 N（<=10^3）、M（<=20）、K（<=5），
分别为推送器松针数量、小盒子容量、每根松枝容量。
第二行给出 N 个正整数，表示推送器按顺序推出的松针大小。

输出格式：
每支成品松枝占一行，按自底向上的顺序输出每片松针大小，数字间 1 个空格。

输入样例：
8 3 4
20 25 15 18 20 18 8 5

输出样例：
20 15
20 18 18 8
25 5
*/

#include<bits/stdc++.h>
using namespace std;
#define debug(x) cout<<#x<<" "<<x<<"\n"

int main() {
	int N, M, K;
	cin>>N>>M>>K;
	queue<int> q;  // 推送器
	while(N--) {
		int x;  cin>>x;
		q.push(x);
	}
	stack<int> box;   // 盒子
	// 只要传送带或者盒子里还有材料，咱们就继续做松枝
	while(!q.empty() || !box.empty()) {
		vector<int> ans;  // 松针
		
		while(ans.size() < K) {
			// 先看盒子
			if(!box.empty() && (ans.empty() || box.top() <= ans.back())) {
				ans.push_back(box.top());
				box.pop();
			} // 再看传送带 
			else if(!q.empty() && (ans.empty() || q.front() <= ans.back())) {
				ans.push_back(q.front());
				q.pop();
			} // 传送带材料不合格，只能扔进盒子
			else if(!q.empty()) {
				if(box.size() < M) {
					box.push(q.front());
					q.pop();
				} else {
					break;  // 盒子满了，且传送带的货装不上去，触发完工条件(1)
				}
			} else { // 盒子不合格，传送带也没货了，触发完工条件(2)
				break;
			}
			// 注意：完工条件(3) branch.size() == K 已经被外层的 while 控制了
		}
		for(int i = 0; i < ans.size(); i++) {
			if(i != ans.size() - 1) cout<<ans[i]<<" ";
			else cout<<ans[i];
		}
		cout<<"\n";
	}
	return 0;
}

// 【总结】
// 一、题型通用模板（受约束的取料模拟）
// 1) 适用特征：有“主序列输入 + 暂存容器 + 当前产物约束”的逐步过程模拟。
// 2) 标准做法：
//    - 主序列用 queue 保证按到达顺序处理；
//    - 暂存盒用 stack 保证后进先出；
//    - 每根成品用数组记录，循环按规则优先级推进，触发结束条件就换下一根。
// 3) 复杂度：每片松针最多进/出一次 queue 或 stack，整体时间 O(N)，空间 O(N+M)。
// 4) 通用易错点：
//    - 忘记“盒子满且不满足”时不能消耗当前推送器元素；
//    - 结束一根后没有正确开启下一根，导致输出粘连。

// 二、本题差异化补充
// 1) 本题的单根约束是“后插不大于前插”，即形成非递增序列。
// 2) K 很小（<=5），但不需要特殊优化，按规则模拟即可。
// 3) 外层条件是“推送器或盒子非空就继续”，可覆盖题面三类结束情形。
