/*
 * L1-058 6翻了
 * 
 * 题目描述：
 * "666"是一种网络用语，大概是表示某人很厉害、我们很佩服的意思。
 * 最近又衍生出另一个数字"9"，意思是"6翻了"，实在太厉害的意思。
 * 如果你以为这就是厉害的最高境界，那就错啦 —— 目前的最高境界是数字"27"，因为这是 3 个 "9"！
 * 
 * 本题就请你编写程序，将那些过时的、只会用一连串"6666……6"表达仰慕的句子，
 * 翻译成最新的高级表达。
 * 
 * 输入格式：
 * 输入在一行中给出一句话，即一个非空字符串，由不超过 1000 个英文字母、数字和空格组成，以回车结束。
 * 
 * 输出格式：
 * 从左到右扫描输入的句子：如果句子中有超过 3 个连续的 6，则将这串连续的 6 替换成 9；
 * 但如果有超过 9 个连续的 6，则将这串连续的 6 替换成 27。
 * 其他内容不受影响，原样输出。
 * 
 * 输入样例：
 * it is so 666 really 6666 what else can I say 6666666666
 * 
 * 输出样例：
 * it is so 666 really 9 what else can I say 27
 * 
 * 解题思路：
 * 1. 先处理超过9个连续6的情况（替换为27），再处理超过3个连续6的情况（替换为9）
 * 2. 为什么顺序重要？如果先处理>3的情况，那么>9的情况也会被替换成9，而不是27
 * 3. 使用 find 找到连续6的起点，然后向后遍历统计6的个数
 * 4. 用 erase + insert 进行替换
 * 
 * 替换规则：
 * - 连续6的个数 > 9：替换为 "27"
 * - 连续6的个数 > 3 且 ≤ 9：替换为 "9"
 * - 连续6的个数 ≤ 3：不替换
 * 
 * 关键技巧：
 * - 必须先处理长串（>9），再处理短串（>3）
 * - 每次替换后更新 pos 位置，避免死循环
 */

#include<bits/stdc++.h>
using namespace std;

int main() {
    string s;
    getline(cin, s);
    
    string s10 = "6666666666";  // 10个6，用于检测>9个连续6
    string s4 = "6666";         // 4个6，用于检测>3个连续6
    
    // 先处理超过9个连续6的情况（替换为27）
    size_t pos = 0;
    while(s.find(s10, pos) != s.npos) {
        pos = s.find(s10, pos);
        size_t i = pos + 10;
        // 继续向后统计还有多少个6
        while(i != s.length()) {
            if(s[i] != '6') break;
            i++;
        }
        // 删除所有连续的6，插入27
        s.erase(pos, i - pos);
        s.insert(pos, "27");
        pos += 2;  // 跳过刚插入的"27"
    }
    
    // 再处理超过3个连续6的情况（替换为9）
    pos = 0;
    while(s.find(s4, pos) != s.npos) {
        pos = s.find(s4, pos);
        size_t i = pos + 4;
        // 继续向后统计还有多少个6
        while(i != s.length()) {
            if(s[i] != '6') break;
            i++;
        }
        // 删除所有连续的6，插入9
        s.erase(pos, i - pos);
        s.insert(pos, 1, '9');
        pos += 1;  // 跳过刚插入的'9'
    }
    
    cout << s;
    
    return 0;
}
