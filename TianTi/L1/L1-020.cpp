/*
L1-020 帅到没朋友

分数 20
作者 陈越
单位 浙江大学

当芸芸众生忙着在朋友圈中发照片的时候，总有一些人因为太帅而没有朋友。本题就要求你找出那些帅到没有朋友的人。

输入格式：
输入第一行给出一个正整数N（≤100），是已知朋友圈的个数；随后N行，每行首先给出一个正整数K（≤1000），
为朋友圈中的人数，然后列出一个朋友圈内的所有人——为方便起见，每人对应一个ID号，为5位数字（从00000到99999），
ID间以空格分隔；之后给出一个正整数M（≤10000），为待查询的人数；随后一行中列出M个待查询的ID，以空格分隔。

注意：没有朋友的人可以是根本没安装"朋友圈"，也可以是只有自己一个人在朋友圈的人。
虽然有个别自恋狂会自己把自己反复加进朋友圈，但题目保证所有K超过1的朋友圈里都至少有2个不同的人。

输出格式：
按输入的顺序输出那些帅到没朋友的人。ID间用1个空格分隔，行的首尾不得有多余空格。
如果没有人太帅，则输出No one is handsome。

注意：同一个人可以被查询多次，但只输出一次。

输入样例1：
3
3 11111 22222 55555
2 33333 44444
4 55555 66666 99999 77777
8
55555 44444 10000 88888 22222 11111 23333 88888

输出样例1：
10000 88888 23333

输入样例2：
3
3 11111 22222 55555
2 33333 44444
4 55555 66666 99999 77777
4
55555 44444 22222 11111

输出样例2：
No one is handsome
*/

#include<bits/stdc++.h>
using namespace std;

int main() {
    int N;  cin>>N;
    unordered_set<int> s;

    while(N--) {
        int k; cin>>k;
        vector<int> v(k);
        for(int i = 0; i < k; i++) {
            cin>>v[i];
        }
        if(k == 1) continue;
        for(int n : v) s.insert(n); // 记录有朋友的人
    }

    int M; cin>>M;
    vector<int> res;

    while(M--) {
        int q; cin>>q;
        if(!s.count(q)) {
            s.insert(q);
            res.push_back(q);
        }
    }

    if(res.size() == 0) cout<<"No one is handsome";
    else {
        for(int i = 0; i < res.size(); i++) {
            if(i > 0) cout<<" ";
            printf("%05d", res[i]);  // 补前导0，输出5位数字
        }
    }

    return 0;
}

/*
【核心思路与讲解】

一、问题的本质
找出"帅到没朋友"的人，即：
1. 从未出现在任何朋友圈中的人
2. 或者只出现在 k=1 的朋友圈中的人（只有自己）

二、关键步骤

1. 记录"有朋友"的人
   - 遍历所有朋友圈
   - 对于 k > 1 的朋友圈，把所有人加入集合 s
   - 对于 k == 1 的朋友圈，跳过（这人算"没朋友"）

2. 查询处理
   - 查询 ID 不在 s 中 → "帅到没朋友"
   - 用 set 去重，保证同一人只输出一次
   - 按查询顺序输出

三、关键点：输出格式

ID 是5位数字（00000-99999），输出时必须补前导0！
- 错误：cout << 888;      // 输出 "888"
- 正确：printf("%05d", 888); // 输出 "00888"

或者改用 string 存储 ID，避免前导0丢失问题。

四、时间复杂度
- 读取朋友圈：O(N × K)，K 最大 1000
- 查询：O(M)，M 最大 10000
- 总复杂度：O(N×K + M)，完全可接受

五、易错点

1. 【陷阱】ID 前导0问题
   用 int 存储 ID，输出时会丢失前导0，必须用 printf("%05d") 补齐。

2. k=1 的处理
   k=1 表示朋友圈只有自己，这人算"没朋友"，不应加入集合 s。

3. 重复查询
   同一人可能被查询多次，要保证只输出一次。

六、替代方案

用 string 存储 ID，更直观：
    unordered_set<string> s;
    string q; cin >> q;
    if(!s.count(q)) { ... }
    cout << q;  // 直接输出，保留前导0
*/
