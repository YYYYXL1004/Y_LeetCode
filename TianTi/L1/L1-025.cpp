// ============ 版本2：重构版本 ============
bool isvaild(string s) {
    if(s.empty() || s.length() > 4) return false;
    for(auto c : s) {
        if(!isdigit(c)) return false;
    }
    int n = stoi(s);
    if(n < 1 || n > 1000) return false;
    return true;
}

int main() {
    string A, B;
    cin >> A;
    getchar(); // 吃掉 A 和 B 中间的那个空格
    getline(cin, B);

    int aa, bb;
    if(isvaild(A)) aa = stoi(A);
    else A = "?";
    if(isvaild(B)) bb = stoi(B);
    else B = "?";

    if(A != "?" && B != "?") cout << aa << " + " << bb << " = " << aa + bb;
    else cout << A << " + " << B << " = " << '?';

    return 0;
}
