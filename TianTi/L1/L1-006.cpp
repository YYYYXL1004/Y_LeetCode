/*
一个正整数 N 的因子中可能存在若干连续的数字。例如 630 可以分解为 3×5×6×7，
其中 5、6、7 就是 3 个连续的数字。给定任一正整数 N，要求编写程序求出最长连续因子的个数，
并输出最小的连续因子序列。

输入格式：
输入在一行中给出一个正整数 N（1<N<2^31）。

输出格式：
首先在第 1 行输出最长连续因子的个数；
然后在第 2 行中按 "因子1*因子2*……*因子k" 的格式输出最小的连续因子序列，
其中因子按递增顺序输出，1 不算在内。

输入样例：
630

输出样例：
3
5*6*7
*/

#include <bits/stdc++.h>
using namespace std;

int main() {
    int N;
    cin >> N;
    
    int maxLen = 0;      // 最长连续因子长度
    int start = 0;       // 最小连续因子序列的起始值
    
    // 枚举连续因子序列的起点 i
    // sqrt(N) 是优化：如果起点超过sqrt(N)，连续两个数相乘就超过N了
    for (int i = 2; i <= sqrt(N); i++) {
        int product = 1;  // 连续乘积
        int j = i;        // 从i开始连续乘
        
        // 尝试连续相乘，直到不能整除N为止
        while (true) {
            product *= j;
            
            // 如果乘积超过N，停止
            if (product > N) break;
            
            // 如果N能被当前连续乘积整除，说明i~j是一个连续因子序列
            if (N % product == 0) {
                int len = j - i + 1;  // 当前连续因子长度
                // 更新最长长度（注意：要找"最小"的序列，所以长度相等时不更新）
                if (len > maxLen) {
                    maxLen = len;
                    start = i;
                }
            }
            j++;
        }
    }
    
    // 特殊情况：N是质数，没有长度>=2的连续因子序列
    if (maxLen == 0) {
        maxLen = 1;
        start = N;  // 此时唯一因子就是N本身
    }
    
    // 输出结果
    cout << maxLen << endl;
    
    for (int i = 0; i < maxLen; i++) {
        if (i > 0) cout << "*";
        cout << start + i;
    }
    cout << endl;
    
    return 0;
}

/*
【核心思路与讲解】

一、问题的本质
找连续因子，就是找一个连续整数序列 [i, i+1, i+2, ..., j]，
使得 i × (i+1) × (i+2) × ... × j 能整除 N。

二、关键优化点

1. 起点只需要枚举到 sqrt(N)
   原因：如果起点 i > sqrt(N)，那么 i × (i+1) > N，不可能有长度>=2的连续因子。
   所以起点范围是 [2, sqrt(N)]。

2. 连续乘积增长很快，内层循环次数很少
   2 × 3 × 4 × 5 × 6 × 7 × 8 = 40320
   连续8个整数相乘就已经4万多了
   对于 N < 2^31（约21亿），最长连续因子长度不会超过 15 左右。
   所以内层while循环次数很少，整体复杂度很低。

3. 为什么用乘积判断而不是逐个判断因子？
   我们维护一个连续乘积 product，如果 N % product == 0，
   说明 product 是 N 的因子，且 product = i × (i+1) × ... × j。
   这意味着 i, i+1, ..., j 都是 N 的连续因子！

三、时间复杂度分析

外层循环：O(sqrt(N))，约 46340 次（当 N = 2^31）
内层循环：每次最多10几次（乘积增长太快）
总复杂度：O(sqrt(N) × 小常数) ≈ O(5×10^5)，完全可接受。

四、特殊情况

如果 N 是质数（如 17），不存在长度>=2的连续因子。
此时需要特判：最长长度为1，唯一因子就是 N 本身。

五、对比：错误的做法

❌ 枚举所有因子，再从中找连续的
   - 找因子需要 O(sqrt(N))，但因子个数可能很多
   - 从因子集合中找最长连续序列也很麻烦

✅ 直接枚举连续整数序列，检查乘积是否能整除N
   - 利用了"连续"的性质，更加直接高效

六、类似题型

这种"找连续因子/约数"的问题，核心都是：
1. 确定搜索范围（通常是 sqrt(N)）
2. 利用连续乘积快速增长来限制搜索深度
3. 用乘积取模来判断是否满足条件
*/
