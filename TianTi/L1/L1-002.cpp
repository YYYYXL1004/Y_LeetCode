/*
本题要求你写个程序把给定的符号打印成沙漏的形状。例如给定17个"*"，要求按下列格式打印

*****

 ***

  *

 ***

*****

所谓"沙漏形状"，是指每行输出奇数个符号；各行符号中心对齐；相邻两行符号数差2；符号数先从大到小顺序递减到1，再从小到大顺序递增；首尾符号数相等。

给定任意N个符号，不一定能正好组成一个沙漏。要求打印出的沙漏能用掉尽可能多的符号。

输入格式:
输入在一行给出1个正整数N（≤1000）和一个符号，中间以空格分隔。

输出格式:
首先打印出由给定符号组成的最大的沙漏形状，最后在一行中输出剩下没用掉的符号数。

输入样例:
19 *

输出样例:
*****
 ***
  *
 ***
*****
2
*/

#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int N;
    char c;
    cin >> N >> c;
    
    // 计算能组成的最大沙漏的层数k
    // 沙漏总符号数 = 2*k*k - 1 (上半部分k层，下半部分k-1层)
    // 等差数列求和 1, 3, 5.. k个数，和为（1+ 2k-1)*k /2 = k^2 , 下半部分k^2 - 1
    int k = (int)sqrt((N + 1) / 2.0);
    
    // 计算实际使用的符号数
    int used = 2 * k * k - 1;
    int remaining = N - used;
    
    // 打印上半部分（包括中间一行）
    for (int i = k; i >= 1; i--) {
        // 打印前导空格
        for (int j = 0; j < k - i; j++) {
            cout << " ";
        }
        // 打印符号
        for (int j = 0; j < 2 * i - 1; j++) {
            cout << c;
        }
        cout << endl;
    }
    
    // 打印下半部分
    for (int i = 2; i <= k; i++) {
        // 打印前导空格
        for (int j = 0; j < k - i; j++) {
            cout << " ";
        }
        // 打印符号
        for (int j = 0; j < 2 * i - 1; j++) {
            cout << c;
        }
        cout << endl;
    }
    
    // 输出剩余符号数
    cout << remaining << endl;
    
    return 0;
}
