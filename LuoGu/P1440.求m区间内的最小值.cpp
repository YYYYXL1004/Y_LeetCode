/*
P1440 求m区间内的最小值

题目描述
一个含有 n 项的数列，求出每一项前的 m 个数到它这个区间内的最小值。
若前面的数不足 m 项则从第 1 个数开始，若前面没有数则输出 0。

输入格式
第一行两个整数，分别表示 n, m。
第二行，n 个正整数，为所给定的数列 a_i。

输出格式
输出 n 行，每行一个整数，第 i 个数为序列中 a_i 之前 m 个数的最小值。

输入样例
6 2
7 8 1 4 3 2

输出样例
0
7
7
1
1
3

说明/提示
对于 100% 的数据，保证 1 <= m <= n <= 2 * 10^6，1 <= a_i <= 3 * 10^7。
*/

#include<bits/stdc++.h>
using namespace std;

int main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);

	int n, m;
	cin >> n >> m;
	vector<int> v(n);
	for(int i = 0; i < n; i++) {
		cin >> v[i];
	}

	deque<int> dq; // 让 v[dq.front()] 保持最小
	// 0 拉出来特判
	dq.push_back(0);
	cout << 0 << "\n";

	// 注意这个窗口是不包括 r 本身的，所以就用 r - 1
	for(int r = 1; r < n; r++) {
		while(!dq.empty() && v[dq.back()] >= v[r - 1]) {
			dq.pop_back();
		}
		dq.push_back(r - 1);

		if(dq.front() <= r - m - 1) { // 当前有效区间是 [r - m, r - 1]
			dq.pop_front();
		}

		cout << v[dq.front()] << "\n";
	}

	return 0;
}

/*
【总结】
1. 通用模板（固定长度历史窗口最小值）
- 适用特征：对每个位置 i，查询它左侧长度至多为 m 的区间最小值/最大值。
- 标准框架：
  a) 双端队列存索引，不存值；
  b) 入新候选时从队尾弹掉更劣元素，保持单调；
  c) 从队头弹掉越过左边界的过期索引；
  d) 队头即当前答案。
- 复杂度结论：每个索引至多入队出队一次，总时间 O(n)，空间 O(m)。
- 通用易错点：
  a) 忘记用索引会导致无法判断元素是否过期；
  b) 左边界和过期条件容易写错；
  c) 最小值队列应保持单调递增（按值）。

2. 本题差异化补充
- 本题查询区间是“a_i 之前 m 个数”，不包含 a_i 自己，所以在位置 r 处入队的是 r - 1。
- 第一个位置前面没有数，按题意单独输出 0，再从 r = 1 开始处理。
- 有效区间写成 [r - m, r - 1]，对应过期条件 dq.front() <= r - m - 1。
*/
